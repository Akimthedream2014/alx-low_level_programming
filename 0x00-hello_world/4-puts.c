#include <stdio.h>
/**
 * main - Entry point of the program
 *
 * This function prints the message "Programming is like building a multilingual puzzle"
 * followed by a newline using the puts function and returns 0.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
    // Define a string containing the message
    char str[] = "Programming is like building a multilingual puzzle";

    // Use the puts function to print the message with a newline
    puts(str);

    // Return 0 to indicate successful execution
    return (0);
}
